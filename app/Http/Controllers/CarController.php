    /**
     * Remove the specified resource from storage.
     *
     * @param  int $id
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $car = Car::findOrFail($id);
        $car->trips()->delete();
        $car->delete();

        return response()->json('Car with ID = ' . $car->id . ' was deleted.', 201);
    }
